package com.chintha.fundamentals.literals;

public class Integralliterals {
	public static void main(String[] args) {
		
		//decimal literals 0 to 9
		int x = 34678;
		long l = 2345678908765l;
		//octal literals 0 to 7 we start with the prefix with 0
		int y = 010;
		//hexadecimal literals 0 to 9 A to Z uppercase and lowercase literal should be prefixed with
		//0x or 0X
		int z = 0x10;
		float f = 12.56f;
		double t = 2343.46; 
		int c = 0xA;
		double d =0xBeef;
		System.out.println(d);
	}

}
